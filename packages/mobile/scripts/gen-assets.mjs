import { writeFileSync, mkdirSync } from 'fs';
import { resolve, dirname } from 'path';

const assetsDir = resolve('packages/mobile/assets');
mkdirSync(assetsDir, { recursive: true });

// Generate a minimal valid 1x1 blue PNG using raw bytes
const png = Buffer.from([
  0x89, 0x50, 0x4e, 0x47, 0x0d, 0x0a, 0x1a, 0x0a, // PNG sig
  0x00, 0x00, 0x00, 0x0d, 0x49, 0x48, 0x44, 0x52, // IHDR
  0x00, 0x00, 0x00, 0x01, 0x00, 0x00, 0x00, 0x01,
  0x08, 0x02, 0x00, 0x00, 0x00, 0x90, 0x77, 0x53, 0xde,
  0x00, 0x00, 0x00, 0x0c, 0x49, 0x44, 0x41, 0x54, // IDAT
  0x08, 0xd7, 0x63, 0xf8, 0x0f, 0x00, 0x00, 0x00,
  0x04, 0x00, 0x01, 0x32, 0x8f, 0xd8, 0x63,
  0x00, 0x00, 0x00, 0x00, 0x49, 0x45, 0x4e, 0x44, // IEND
  0xae, 0x42, 0x60, 0x82
]);

const files = ['icon.png', 'splash.png', 'adaptive-icon.png', 'favicon.png'];
for (const f of files) {
  const fp = resolve(assetsDir, f);
  writeFileSync(fp, png);
  console.log(`Created ${fp}`);
}
